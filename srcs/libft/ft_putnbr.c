#include "libft.h"

void	ft_putnbr(int n)
{
	long int r;

	r = n;
	if (r < 0)
	{
		r *= -1;
		ft_putchar(45);
	}
	if (r > 9)
	{
		ft_putnbr(r / 10);
	}
	ft_putchar(r % 10 + 48);
}
